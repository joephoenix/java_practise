package phoenix.human;

abstract class Human {
	protected String sex;
	protected String name;

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSex() {
		return sex;
	}

	abstract void doPee();
}
